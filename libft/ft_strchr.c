char	*strchr(const char *s, int c)
{
	char	*a;
	int		i;

	i = 0;
	a = (char *)a;
	while (s[i] != '\0')
	{
		if (s[i] == c)
		{
			return (a + i);
		}
		i++;
	}
	if (c == '\0')
	{
		return (a + i);
	}
	return (0);
}
